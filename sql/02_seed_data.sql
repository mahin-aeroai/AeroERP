-- ── MODULE PERMISSIONS FOR ALL ROLES ─────────────────────────
-- SuperAdmin: all CRUD+Export on all modules
BEGIN
  FOR m IN (SELECT COLUMN_VALUE AS MC FROM TABLE(SYS.ODCIVARCHAR2LIST(
    'COMPANY','CONTACT','OPPORTUNITY','JOB','TECHNOLOGY','SUPPLIER','PROJECT',
    'DOCUMENT','CRM_ACTIVITY','DASHBOARD','REPORTS','USERS','SETTINGS'))) LOOP
