    VALUES ((SELECT ROLE_ID FROM ERP_ROLES WHERE ROLE_NAME='SuperAdmin'),
            m.MC, 'Y','Y','Y','Y','Y');
  END LOOP;
END;
/

-- Analyst: CRUD on Company/Contact/Job/Tech, view on Opp/Project
