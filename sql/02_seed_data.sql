    VALUES ((SELECT ROLE_ID FROM ERP_ROLES WHERE ROLE_NAME='Analyst'),
            m.MC, 'Y','Y','Y','N','Y');
  END LOOP;
  FOR m IN (SELECT COLUMN_VALUE AS MC FROM TABLE(SYS.ODCIVARCHAR2LIST(
    'OPPORTUNITY','PROJECT','SUPPLIER','DOCUMENT'))) LOOP
    INSERT INTO ERP_MODULE_PERMISSIONS (ROLE_ID, MODULE_CODE, CAN_VIEW, CAN_CREATE, CAN_EDIT, CAN_DELETE, CAN_EXPORT)
